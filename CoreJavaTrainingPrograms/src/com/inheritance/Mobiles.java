package com.inheritance;

public class Mobiles {

	int cost;
	String colour;
	String RAM;
	
	//Write getters and setters for the above mentioned instance variables
	public int getCost() {
		return cost;
	}
	public void setCost(int mobileCost) {
		this.cost = mobileCost;
	}
	
	public String getColour() {
		return colour;
	}
	public void setColour(String mobileColour) {
		this.colour = mobileColour;
	}
	
	public String getRAM() {
		return RAM;
	}
	public void setRAM(String ramValue) {
		this.RAM = ramValue;
	}
}
