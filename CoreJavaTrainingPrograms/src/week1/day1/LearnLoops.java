package week1.day1;

public class LearnLoops {

	public static void main(String[] args) {
		
		//For Loop: Print ODD number from 1 to 100
		for(int i=1; i<=100; i+=2)
		{
			System.out.print(i);
		}
		System.out.println();
		
		//While Loop
		int i =1;
		while(i<100)
		{
			System.out.println(i);
			i++;
		}
		
		//Printing Boolean output
		int a =5; int b =10;
		System.out.print(a>b);

	}

}
